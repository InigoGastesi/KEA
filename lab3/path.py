#Grafoaren sakonerako zeharkatzea: BFS Breadth-first search
def graph_has_a_path(graph, start, end):
    nodoak=[start]
    bisitatutakoak=[start]
    #aztertu behar diren nodoak dauden bitartean begizta hau egingo da
    while(len(nodoak)!=0):
        nodo = nodoak[0]
        nodoak.remove(nodoak[0])
        #aztertzen ari den nodoa bukaerako nodoa denean, bi nodoen artean bidea dago beraz True bueltatu
        if(nodo==end):
                return True
        #aztertzen ari den nodoaren auzokideak aztertu
        for i in range(0, len(graph[nodo])):
                #ondoko nodoak ez badira bisitatu, aztertu behar diren nodoetara gehitu
                if (graph[nodo][i] == 1 and not i in bisitatutakoak):
                    bisitatutakoak.append(i)
                    nodoak.append(i)
    return False

